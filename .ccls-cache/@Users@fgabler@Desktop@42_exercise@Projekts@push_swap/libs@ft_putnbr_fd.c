#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	if (n < 0 && n > -2147483648)
	{
		n = n * -1;
		write (fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	if (n < 10 && n > -2147483648)
	{
		c = n + 48;
		write (fd, &c, 1);
	}
}
